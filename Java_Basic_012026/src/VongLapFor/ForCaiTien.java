package VongLapFor;

public class ForCaiTien {

    public static void main(String[] args) {

        int array1[] = new int[]{5,10,15,20,25,30};

        //Khai báo FOR cải tiến để duyệt mảng hoặc là array list
        for (int tenbien: array1) {
            System.out.println(tenbien);
        }

    }

}
